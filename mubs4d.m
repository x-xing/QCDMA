function out = mubs4d(n)
mubs4 = zeros(4,4,5);
mubs4(:,:,1) = eye(4);
mubs4(:,:,2) = [0.5*[1, 1, 1, 1]; 0.5*[1, 1, -1, -1]; 0.5*[1, -1, -1, 1]; 0.5*[1, -1, 1, -1]];
mubs4(:,:,3) = [0.5*[1, -1, -i, -i]; 0.5*[1, -1, i, i]; 0.5*[1, 1, i, -i];0.5* [1, 1, -i, i]];
mubs4(:,:,4) = [0.5*[1, -i, -i, -1]; 0.5*[1, -i, i, 1]; [1, i, i, -1]; 0.5*[1, i, -i, 1]];
mubs4(:,:,5) = [0.5*[1, -i, -i, -1]; 0.5*[1, -i, i, 1]; 0.5*[1, i, i, -1]; 0.5*[1, i, -i, 1]];

out = mubs4(:,:,n);